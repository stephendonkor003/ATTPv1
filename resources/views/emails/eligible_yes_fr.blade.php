            </p>

            <p>L’évaluation à venir sera menée par le <strong>Consortium Indépendant d’Experts (ICE)</strong>, hébergé
                par la <strong>Commission de l’Union Africaine (CUA)</strong>. Au cours de cette phase, les propositions
                seront rigoureusement évaluées selon les critères suivants :</p>

            <ol>
                <li><strong>Qualité et Pertinence (50%)</strong> : La qualité de la proposition et son alignement avec
                    les objectifs de l’ATTP.</li>
                <li><strong>Capacité (30%)</strong> : La capacité du personnel clé et des systèmes opérationnels à
                    mettre
                    en œuvre les activités proposées.</li>
                <li><strong>Budget (20%)</strong> : La faisabilité et la pertinence du budget proposé.</li>
            </ol>

            <p>
                Les propositions doivent obtenir un score minimum de <strong>70%</strong> pour être prises en
                considération pour le financement. Les scores obtenus guideront l’ICE dans ses recommandations au
                <strong>Comité de Pilotage de la Plateforme des Think Tanks (TTPSC)</strong> pour approbation finale.
            </p>

            <p>
                Dans le cadre du processus d’évaluation, des visites sur site seront effectuées par des représentants de
                l’ICE et de la CUA auprès des <strong>3 à 5 think tanks les mieux classés</strong>. Ces visites
                permettront d’évaluer la capacité et la préparation des consortiums à mettre en œuvre efficacement les
                activités proposées.
            </p>

            <p>
                Après sélection, les consortiums devront soumettre un budget annuel plus détaillé afin de finaliser
                l’accord de subvention.
            </p>

            <p>
