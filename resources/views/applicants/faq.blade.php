            <h3><strong>A. PROJECT OVERVIEW & BACKGROUND</strong></h3>

            <p> <strong> What is the primary objective of the Africa Think Tank Platform (ATTP) project? </strong><br>
                The ATTP project aims to strengthen the capacity of the African Union Commission (AUC) for policymaking
                and implementation by harnessing resources from national and continental think tanks. It focuses on
                translating the ambitions of Agenda 2063 into tangible development outcomes for the continent, fostering
                an integrated, prosperous, and peaceful Africa. Ultimately, this project is intended to operate as a
                sustainable policymaking tool.
            </p>

            <p><strong>2. Who are the beneficiaries of the ATTP project?</strong> ,<br>

                The direct and immediate beneficiaries are the relevant stakeholders who gain from support aimed at
                strengthening policy-making capacity. This includes regional entities such as the AUC, its specialized
                agencies, and Regional Economic Communities (RECs), African governments, think tanks, and Africans as
                the ultimate beneficiaries. Participating think tanks will receive various benefits, including support
                for research and analytical work on cross-boundary policy issues, policy engagement, policy adoption,
                and institutional capacity building. It is expected that approximately 15 think tanks, about 1 percent
                of the 1,300 across the continent, will directly benefit from the project.
            </p>

            <p><strong> How is the call for proposals linked to the ATTP Project?</strong><br>

                The project serves as a critical enabler of Africa's economic transformation by promoting and
                accelerating the uptake of relevant policies identified by think tanks. African think tanks will be
                invited to submit proposals that present practical solutions, highlight evidence, demonstrate strong
                potential for impact, and align with the project's objectives. To realize this vision, the project will
                be executed in three components:

                <br>
                <li> Component I: Establish capacity to operate a sustainable policymaking platform.</li>
                <li> Component II: Strengthen the quality, relevance, and uptake of policy research on priority issues.
                    Through a competitive, merit-based process, the project will award grants to 2-4 think tank
                    consortia to
                    further this goal.</li>
                <li>Component III: Support the sustainability of the regional platform.
                    The call for proposals responds to Component II of the project.
                    For more information on the components and project, please visit the ATTP project portal on the
                    World
                    Bank Website.</li>

            </p>

            <p>

                <strong> 4. What are the priority thematic areas covered under the project?</strong> <br>
                The ATTP project will initially target six priority thematic areas that are aligned with the AU’s Agenda
                2063 and other initiatives that articulate continent priorities, such as the Sustainable Development
                Goals and International Development Association (IDA) regional priorities:

                <li>i. Economic transformation and governance, including issues around debt and illicit financial flows.
                </li>
                <li>ii. Climate change, including the energy transition, adaptation capacity, and carbon markets.</li>
                <li>iii. Regional trade with a focus on the African Continental Free Trade Area (AfCFTA) and promotion
                    of
                    regional value chains</li>
                <li>iv. Food security with a focus on finding solutions to address hunger and ensure food availability
                    throughout the continent.</li>
                <li>v. Human capital development with an emphasis on investing in education, health, and skills
                    development.</li>
                <li> vi. Digitalization to leverage technology for development and innovation.</li>

            </p>


            <p>
                <strong>5. What are the key areas for which the think tank consortia are expected to implement
                    activities?</strong>

                The consortia are expected to implement activities: <br>

                <li>i. Producing relevant, good-quality, and collaborative policy research on continental priorities:
                    This
                    area focuses on generating high-quality, collaborative research on key continental priorities in the
                    six
                    thematic areas, identifying and bridging critical gaps in the policy cycle, including design,
                    implementation, evaluation, and feedback. Research must be relevant to the challenges faced on the
                    continent and in the policymaking cycle.</li>
                <br>
                <li>ii. Developing and implementing activities for effective policy engagement and influence regionally
                    and
                    nationally: This area focuses on developing strategies to promote more effective engagement at the
                    regional and national levels, aiming to influence policy formulation and implementation at the
                    continental and country levels. Strategies must also seek to strengthen think tanks’ efforts to
                    enhance
                    the communication, dissemination, and outreach of their policy research, analysis, and
                    recommendations,
                    ensuring uptake by relevant stakeholders.
                    <br>
                    <strong>Note: </strong> research and policy engagement supported by the ATTP project will need to
                    demonstrate: (i)
                    that it has relevance for more than one country in the region; and (ii) is expected to generate
                    significant positive externalities or mitigate negative externalities through, for example, its
                    regional/cross-border approach to policymaking.
                </li>
                <br>
                <li>iii. Building institutional capacity to enhance sustainability: This area aims to build the
                    long-term
                    sustainability of think tanks by supporting activities that improve financial sustainability plans,
                    strengthen research capacity, promote staff retention, and foster the development of young
                    researchers
                    and their capacity pipelines.</li>
                <br>
                <li>iv. Enhancing the representation of female policy professionals: This area focuses on promoting
                    gender
                    inclusivity and increasing female representation in the policy arena, providing mentorship,
                    visibility,
                    outreach, and supportive environments.</li>
            </p>
            <h3> B. FINANCIAL MANAGEMENT</h3>


            <p> <strong> How is the ATTP project financed, and what is the total funding amount?</strong>
            <p>

            <p> The ATTP project is financed through an International Development Association (IDA) grant from the World
                Bank Group, amounting to US$50 million. The project life cycle is from July 2024 to June 2028.</p>

            <p> <strong> 7. What are the key components of financial management within the ATTP project?</p> </strong>

            <p>Financial Management (FM) for the project involves several crucial aspects, including budgetary controls,
                clearly defined procedures, managed fund flows/disbursements, a robust accounting system, qualified FM
                staffing, effective information/accounting systems, internal controls, financial reporting, auditing
                arrangements, adherence to FM covenants, and a detailed FM Action Plan. This includes ensuring that
                Annual
                Work Plans and budgets (AWP&B) are submitted for the Bank's No Objection and closely following up on
                budget
                utilization. A finance officer will be recruited or assigned to the program, and relevant account codes
                will
                be maintained in the Applications and Processing (SAP) system.</p>

            <p> <strong>8. What procurement guidelines and procedures must be followed within the ATTP project?</p>
            </strong>

            <p>Procurement within the ATTP project must adhere to the World Bank's "Procurement Regulations for
                Investment
                Project Financing (IPF) Borrowers," specifically the Fourth Edition, November 2020. This includes
                preparing
                a detailed procurement plan, utilizing World Bank standard bidding documents, following up on
                solicitation
                documents with the Bank, and adhering to guidelines for international competitive bidding (ICB), bid
                evaluation, and contract award procedures. The Operations Support Services Directorate (OSSD/SCMD) is
                responsible for consolidating procurement plans and submitting them to the World Bank for review and
                approval through the World Bank’s internal procurement approval system, Systematic Tracking of Exchanges
                in
                Procurement (STEP).</p>


            <p> <strong> 9. What are the requirements for handling complaints related to procurement under the ATTP
                    project?</p> </strong>

            <p> Complaints arising from national procurement procedures when approaching the national market may be
                handled
                in accordance with the Borrower’s national procurement complaints review procedures, if those provisions
                are
                acceptable to the Bank. However, for contracts following national procurement procedures that are
                identified
                to be prior reviewed by the Bank, paragraphs 3.2 to 3.5 of Annex III of the Procurement Regulations
                shall
                also apply. Complaints arising under contracts where Bank Standard Procurement Documents (SPDs) are
                required
                will be handled in accordance with Annex III of the Procurement Regulations.</p>

            <p><strong>10. What role does the Project Procurement Strategy for Development (PPSD) play in the ATTP
                    project?</strong></p>

            <p> The PPSD is a crucial document of AUC that outlines how procurement activities will support the
                development
                objectives of the ATTP project and deliver the best Value for Money (VfM) under a risk-based approach.
                It
                justifies the selection methods in the Procurement Plan and ensures that procurement decisions are
                strategically aligned with project goals.</p>

            <p><strong> 11. What are the conditions for the disbursement of funds to selected think tanks?</strong></p>

            <p> Disbursement of funds to the successful think tanks will only occur after the AU has internally approved
                the
                submitted agreed deliverables. The AUC will register each selected think tank as a vendor in the AUC SAP
                system and assign a vendor ID. Upon approval of the submitted deliverables, the think tank will submit
                an
                invoice to the ATTP project secretariat for processing. AUC Finance will process the payment for each
                approved deliverable submitted by the think tank within 30 working days.</p>

            <p><strong>12. What are the conditions for disbursement of funds related to the Endowment Fund within the
                    ATTP
                    project?</strong></p>

            <p> Disbursement to the Endowment Fund will only occur after AUC has internally approved and
                established the
                Endowment Fund through its internal policy organs and appointed a Fund Manager, all in a manner
                satisfactory
                to the AUC.</p>

            <p> <strong>13. What measures are in place to address fraud, corruption, and conflicts of interest during
                    procurement
                    in
                    the ATTP project?</strong></p>

            <p> ATTP project incorporates measures to prevent and address fraud, corruption, and conflicts of
                interest.
                The Bank can reject proposals, sanction firms or individuals, and require audits to ensure
                transparency
                and
                accountability. Firms or individuals declared ineligible or sanctioned by the Bank are excluded from
                participation. Bidders are required to permit the Bank to inspect all accounts, records, and other
                documents
                relating to the procurement process. </p>

            <h3> C. PROJECT GOVERNANCE</h3>


            <p><strong> 14. What is the governance mechanism of the Project, and what is its role? </p></strong>

            <p>The main governance body of the ATTP is the Think Tank Platform Steering Committee (TTPSC). The TTPSC's
                role
                is to provide strategic guidance and oversight for project implementation, ensuring effective
                coordination
                and collaboration among participating AUC departments and directorates.</p>

            <p><strong> 15. What is the composition of the TTPSC? </p></strong>

            <p> The TTPSC will comprise 12 members and have pan-African representation, including representatives from
                the
                AUC, AU organs, and specialized agencies, as well as representatives of relevant regional bodies
                (Regional
                Economic Communities, the private sector, academia), and development partners supporting the AUC.</p>

            <p><strong>16. What are the frequency and modality of TTPSC meetings? </p></strong>

            <p> The TTPSC is expected to meet twice a year. This may include a meeting at the beginning of the year to
                discuss and approve work plans and budgets, and one at the end of the year to review annual reports.
                However, additional meetings may be required, for example, during the selection of think tanks and
                addressing other implementation issues as they arise. Meetings may be conducted in person or virtually,
                depending on the circumstances.</p>

            <p><strong>17. How are decisions made during TTPSC meetings? </p></strong>

            <p> Decision-making will be made through majority vote. That requires support from more than 50% of the
                members
                who attend the meeting if there is a quorum. In the event of a tie, the Chair of the TTPSC will cast the
                decisive vote.</p>

            <h3> D. THINK TANK CONSORTIUM SUBMISSION</h3>

            <p> <strong> Who can submit a proposal? </p></strong>
            <p> Only a consortium lead may submit a proposal on behalf of the consortium. Individual organizations are
                not
                eligible to apply.</p>

            <p> <strong>19. How are proposals submitted? </p> </strong>

            <p>Proposals must be submitted through the ATTP project platform: https://africathinktankplatform.africa.
                Only
                complete proposals submitted by consortia will advance to the review stage.</p>

            <p> <strong> 20. How will the applications be submitted? </p> </strong>

            <p> Applicants are required to use the provided templates and submit their applications (application
                website) by
                the specified deadline.</p>

            <p> <strong> 21. What documents are required, and how do I ensure my proposal is complete? </p> </strong>

            <p>Required Documents: All Interested Think Tanks must include the following information as part of their
                proposal submission:
                <li> 1) Completed ATTP Application Form</li>
                <li> 2) Project work plan and budget (up to four years)</li>
                <li> 3) Curriculum vitae (CV) of consortium coordinator</li>
                <li> 4) CV of deputy consortium coordinator</li>
                <li> 5) CVs of research team members (top 2 experts from each think tank)</li>
                <li> 6) Past research and engagement experience</li>
                <li> 7) Signed commitment letter from all consortium members and the lead think tank confirming their
                    agreement
                    to the consortium plan and active participation in its execution</li>
                <li> 8) A copy of the legal registration for each consortium member</li>
                <li>9) Board of trustees’ structure for each consortium member</li>
                <li> 10) Audited financial reports (up to three years) for each consortium member
                    All documents listed in the 'Required Documents' section are mandatory. All interested Think Tanks
                    can
                    download the call for proposal documents (1-7) using the ‘Download Materials’ tab via:
                    https://africathinktankplatform.africa/apply.
                    Incomplete proposals will not be accepted or advanced to the evaluation stage. Refer to the
                    checklist to
                    confirm all required materials are included before submission.</li>
            </p>

            <p> <strong>22. Can I revise my proposal after it has been submitted? </p> </strong>

            <p> Yes. Proposals can be edited once submitted before the deadline for submissions. Successful applications
                will be granted login credentials, which can be used to log in to the ATTP platform via the following
                link:
                https://africathinktankplatform.africa/login. Applicants will only be given the option to edit and
                upload
                ‘Required Documents.’</p>

            <p> <strong>23. How will I know if my proposal was received and accepted? </p> </strong>
            <p>Applicants will receive a confirmation email upon submission using the official email they registered
                with
                on the ATTP platform. Notifications regarding completeness will be sent after the deadline.</p>

            <p> <strong> 24. How can I get help during the proposal preparation process? </p> </strong>
            <p>Weekly informational webinars and open sessions will be organized regularly to support applicants. All
                registered applicants will get a notification through their registered email addresses. Notifications
                will
                also appear on the ATTP website page: https://africathinktankplatform.africa/events.</p>

            <p> <strong>25. What languages does the application support? </p> </strong>

            <p>The proposals should be submitted in English. Applicants can include an additional copy in one of the
                official African Union working languages (Arabic, French, Portuguese, Spanish, or Kiswahili).</p>

            <p> <strong> 26. How many proposals can be submitted per consortium? </p> </strong>

            <p> Only one proposal per consortium is allowed, and only complete applications will be considered.</p>

            <h3>E. THINK TANK CONSORTIUM ELIGIBILITY & SELECTION</h3>

            <p> <strong> 27. What are the key eligibility criteria that will be used for the selection of the think
                    tanks? </p> </strong>

            <p>To be eligible for this project, applicants must meet the following criteria:

                <li> Apply as a consortium comprised of 3-5 African think tanks, with a lead think tank.</li>
                <li> Think tanks are defined as independent policy research organizations registered in continental
                    Africa and
                    engaged in public policy research and engagement. The application must include documented evidence
                    of at
                    least three years of operation of all member think tanks (including the lead think tank) since the
                    think
                    tank’s official constitution was established, including legal registration, a board of trustees, and
                    audited
                    financial reports.</li>
                <li>Applications include comprehensive activities in at least four of the six priority themes. The
                    application
                    must clearly indicate the priority themes it covers.</li>
                <li> Demonstrate a strong commitment to aligning with and contributing to regional integration
                    priorities,
                    which involve addressing regional and cross-border issues, harmonizing research and policy
                    endeavors, and
                    supporting regional cooperation.</li>
                <li> Submit a signed Commitment Letter from all consortium members and the lead organization confirming
                    adherence to the consortium plan and commitment to ensuring participation and task execution
                    according to
                    the plan.</li>
            </p>

            <p> <strong>28. How are think tanks and a consortium of think tanks defined in the context of the ATTP
                    Project? </p> </strong>

            <p> In the context of this project, a consortium refers to a collaborative association specifically
                comprised of
                African research think tanks.</p>

            <p> Lead Think Tank: In addition to serving as a member of the consortium, the Lead Think Tank will steer
                and
                coordinate the consortium’s activities. This will involve providing strategic direction and guiding the
                consortium's endeavors, facilitating cooperation, and ensuring the accomplishment of shared objectives,
                including aiding fellow consortium members in achieving project goals.</p>

            <p>Consortium Member Think Tanks: Consortium Member Think Tanks are the entities within the consortium,
                excluding the Lead Think Tank. They undertake a range of roles, including activities outlined in the
                consortium proposal, such as research, policy engagement, capacity building, and promoting inclusivity
                for
                female policy professionals. These think tanks also serve as focal points for specific themes, engaging
                in
                technical discussions with AUC thematic directorates. Researchers, analysts, and experts from each
                member
                think tank must actively contribute their expertise to collective efforts, fostering a dynamic
                environment
                for collaborative research and insights on regional and continental topics.</p>

            <p> <strong>29. How is the Consortium of Think Tanks screened and selected, and what processes guide this
                    selection? </p> </strong>

            <p>The selection process will entail an initial screening step to ensure that the submitted proposals meet
                the
                eligibility criteria. After the initial screening process, an Independent Committee of Experts (ICE)
                will
                undertake a two-step review process involving a desk review followed by site visits to successful
                consortia.
            </p>

            <p>After the initial screening process, an Independent Committee of Experts (ICE) will undertake a two-step
                review process, involving a desk review followed by site visits.</p>

            <p>Screening: After the submission deadline, AUC staff will screen all proposals to ensure eligibility. Only
                applications that meet the eligibility criteria set forth in the call for proposals will proceed to the
                independent review stage. Each proposal will be carefully evaluated for completeness, compliance with
                the
                requirements, and adherence to the proposal format and guidelines. Any applications that do not meet the
                eligibility criteria will be disqualified. Applicants will receive notification of screening results and
                instructions for the independent review process if they are selected to advance to the next stage.</p>

            <p>After the initial screening step, an ICE hosted by the AUC will review all eligible proposals submitted
                for
                consideration. The ICE will evaluate the proposals based on a set of criteria, and each proposal will
                receive a score for three key areas: the quality of the proposal and its relevance to the ATTP
                objectives,
                the capacity of key personnel and operational systems to implement the proposed activities, and the
                proposed
                budget. The evaluation results will guide the ICE in recommending the final proposals for funding to the
                project’s key decision-making body, the Think Tank Platform Steering Committee (TTPSC), for approval.
                Selected think tank consortia will undergo an institutional capacity assessment to develop and
                supplement
                existing capacity-building activities. A critical area of grant support will be to help think tanks
                strengthen their financial sustainability. Receipt of funds will be contingent upon the think tank
                consortia's agreement to implement recommendations from these assessments. Consortia will also be
                required
                to develop a more detailed annual budget upon selection. Proposals will be evaluated based on a set of
                criteria, and each proposal will receive a score in the various areas.</p>

            <p>Site visits: The TTPSC will utilize the scores obtained to decide which proposals merit funding. Before
                selecting the consortium of think tanks, ICE, representatives from AUC, and independent observers from
                international think tanks will conduct site visits to assess the capacity and capability of the finalist
                think tanks to undertake the proposed work.</p>

            <p> <strong>30. What are the key timelines of the selection process? </p> </strong>

            <p> The selection process is expected to have the following timeline:</p>

            <li> Proposal submission deadline: 24 September 2025</li>
            <li> Screening: 1 October 2025</li>
            <li> Evaluation and selection of proposals: 17 October 2025</li>
            <li> Award notification: 14 November 2025</li>
            <li> Grant disbursement: 19 December 2025</li>


            <h3> F. ATTP FUND DISBURSEMENT AND IMPLEMENTATION</h3>


            <p><strong> 31. How will funds be allocated to the selected consortium of Think Tanks?</p></strong>

            <p> Think tank consortia will be selected depending on the quality of proposals and availability
                of funds. It is
                expected that the ATTP project will provide support to 3-5 competitively selected think tank
                consortia,
                granting a set amount per consortium over a 4-year period.</p>

            <p>Each consortium’s funding will adhere to the following principles:</p>

            <li>i. Think tanks within each consortium can receive a maximum of 30% of their annual operating
                budget for each
                of the project’s four years.</li>

            <li>ii. The maximum grant for a consortium is up to $10 million during the project life cycle.</li>

            <p><strong> 32. How will the consortium's performance be monitored and measured under the ATTP Project?</p>
            </strong>

            <p>The Think Tank’s performance will be measured by its success in meeting or surpassing the
                set of performance
                targets stipulated in the Project Results Framework. To assess overall performance, progress
                toward
                achieving the end target will be measured annually. The methodology for measuring
                performance shall focus on
                the difference between the planned target and the actual performance. Therefore, the Think
                Tank must set
                realistic targets.</p>


            <p>The Project performance and funding utilization shall be reviewed annually. If a think-tank
                fails to meet
                expected results for a given year (Year N) and demonstrates appropriate use of allocated
                funds (See Annex
                1), the AUC and TTPSC may withhold up to 10% of the think-tank’s funding for the following
                year (Year N+1).
                These withheld funds can be released to the Think Tank upon full completion of Year N
                activities.</p>

            <p>The project’s mid-term review shall serve as an opportunity to reallocate funds based on
                performance in
                relation to achieving targets and the effective use of project financing.</p>

            <li> i. If performance against agreed-upon targets after two years of implementation is less than
                75%, then
                financing shall be reduced by at least 10% of uncommitted funds, unless otherwise agreed
                under exceptional
                circumstances. </li>

            <li>ii. If the proportion of funding used after two years of implementation is less than 50%,
                the financing
                shall be reduced by a maximum of 10% of the uncommitted amount that exceeds 50% of the
                funding, unless
                otherwise agreed upon under special circumstances.</li>

            <p>The combination of i) and ii) cannot lead to a cut of more than 10% of future funding.</p>

            <p> Should a think tank not show evidence of progress toward achieving stipulated performance
                targets according
                to the timeline for implementation detailed in Annex 1 result framework or of use of funds,
                the AUC and the
                TTPSC, following consultation with the World Bank, may re-allocate funding from a
                non-performing Think Tank
                to other Think Tanks or project components with the mid-term review serving as the mechanism
                for
                re-allocation of funds that provide room for sanctions and rewards based on performance and
                achievement of
                agreed upon targets.</p>

            <p><strong> 33. How are disputes under the project settled?</p></strong>

            <p> Any dispute arising under this Agreement, which cannot be amicably settled between the
                parties to this
                Agreement, shall be referred to the Think Tank Platform Steering Committee, which is the
                arbiter of all
                disputes. The decision of TTPSC is final.</p>

            <p><strong> 34. Will the ATTP Secretariat organize an informational session for potential applicants?</p>
            </strong>

            <p>The AUC will organize informational sessions to assist potential applicants of the ATTP
                project. These
                sessions are designed to support consortia of think tanks interested in applying for the
                project, providing
                essential knowledge and guidance on the program requirements, grant proposal development,
                and the proposal
                submission process. It is strongly encouraged that all eligible think tank consortia attend
                these sessions
                to engage with field experts and have their questions addressed. By organizing these
                sessions, the AUC aims
                to create a platform for participants to learn, exchange experiences, gain a comprehensive
                understanding of
                the project objectives, and ultimately submit stronger proposals.</p>

            <p><strong> 35. Who and how to contact the ATTP Secretariat if the applicants have additional questions
                    or requests for
                    clarification?</p></strong>

            <p> For further information or clarification, applicants are invited to contact the ATTP
                Secretariat using this
                email address: attpinfo@africanunion.org</p>










